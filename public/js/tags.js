    function cityExists(words) {
        for(var i = 0; i < words.length; i++){
            for(var key = 0; key < city.length; key++){
                if((city[key].text.length == words[i].length) && (city[key].text.toLowerCase().indexOf(words[i]) == 0)){
                    return true;
                }
            }
        }
        return false;
    }
    
