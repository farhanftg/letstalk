    function getBudgetSuggestion(str){
        var makeExists      = false;
        var modelExists     = false;
        var minPrice        = 1000;
        var maxPrice        = 0;
        var makeMinPrice    = 1000;
        var makeMaxPrice    = 0;
        var modelMinPrice   = 1000;
        var modelMaxPrice   = 0;
        var budgetChoices   = new Array();
        
        str         = str.toLowerCase();
        str         = str.trim();
        var words   = str.split(" ");
        var data    = words[words.length-1];
        if(words.length > 1){
            if(data.toLowerCase() == 'than'){
                var word = words[words.length-2];
                if(word.toLowerCase() == 'more' || word.toLowerCase() == 'less'){
                    data = word + ' ' + data;
                }
            }
        }  
                
        for(var i = 0; i < words.length; i++){
            for(var key = 0; key < model.length; key++){
                if((model[key].make.length == words[i].length) && (model[key].make.toLowerCase().indexOf(words[i]) == 0)){
                    if(model[key].minPrice >= 0){
                        if(makeMinPrice > model[key].minPrice){
                            makeMinPrice = model[key].minPrice;
                        }
                    }
                    if(model[key].maxPrice >= 0){
                        if(makeMaxPrice < model[key].maxPrice){
                            makeMaxPrice = model[key].maxPrice;
                        }
                    }
                    makeExists = true;
                }
                if((model[key].text.length == words[i].length) && (model[key].text.toLowerCase().indexOf(words[i]) == 0)){
                    if(model[key].minPrice >= 0){
                        if(modelMinPrice > model[key].minPrice){
                            modelMinPrice = model[key].minPrice;
                        }
                    }
                    if(model[key].maxPrice >= 0){
                        if(modelMaxPrice < model[key].maxPrice){
                            modelMaxPrice = model[key].maxPrice;
                        }
                    }
                    modelExists = true;
                    break;
                }
            }
        }
        if(makeExists){
            minPrice = makeMinPrice;
            maxPrice = makeMaxPrice;
        }
        if(modelExists){
            minPrice = modelMinPrice;
            maxPrice = modelMaxPrice;
        }
              
        if(maxPrice > 0){
            var lowerPrice      = 0;
            var upperPrice      = 0;
            var budgetChoice    = new Object;
            var value           = '';           
            var diff            = maxPrice - minPrice;
            var priceInterval   = diff/4;
            priceInterval       = Math.round(priceInterval);
            minPrice            = Math.round(minPrice);
            maxPrice            = Math.round(maxPrice);
            lowerPrice          = minPrice;
            if(priceInterval <= 0){
                priceInterval = 1;
            }
            while(upperPrice < maxPrice){
                upperPrice      = lowerPrice + priceInterval;
                if(upperPrice > maxPrice){
                    upperPrice = maxPrice;
                }
                if(data == 'above' || data == 'more than'){
                    budgetChoice    = lowerPrice + ' Lac';
                }else if(data == 'under' || data == 'below' || data == 'within' || data == 'less than'){
                    budgetChoice    = upperPrice + ' Lac';
                }else{
                    if(lowerPrice == 0 && data != 'between'){
                        budgetChoice    = ' under ' + upperPrice + ' Lac';
                    }else{
                        budgetChoice    = lowerPrice + ' to ' + upperPrice + ' Lac';
                    }
                }
                lowerPrice          = upperPrice;
                budgetChoices.push(budgetChoice);
            }
        }
        //console.log(str+' '+ minPrice + ' ' + maxPrice);
        //console.log(budgetChoices);
        return budgetChoices;
    }
    
    
